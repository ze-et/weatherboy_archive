void Adafruit_SSD1306::print(char character)
{
  int16_t c_idx = character - font->first;
  if(c_idx < 0){
    return;
  }
  if(character > font->last){
    return;
  }
  auto const& glyph = font->glyph[c_idx];
  uint16_t b_idx = glyph.bitmapOffset;
  uint8_t* bitmap = &(font->bitmap[b_idx]);
  uint8_t x = cursor_x + glyph.xOffset;
  uint8_t y = cursor_y + glyph.yOffset;
  bool const is_font_legacy = glyph.width == 8 || glyph.height == 5;

  for(int y_i = 0; y_i < font_size; y_i += 1){
    for(int x_i = 0; x_i < font_size; x_i += 1){
      if(is_font_legacy){
        drawBitmap(x + x_i,     y - y_i, bitmap,
                   glyph.width, glyph.height, font_color, font_size, true);
      }
      else{
        drawBitmap(x + x_i,     y + y_i + 4, bitmap,
                   glyph.width, glyph.height, font_color, font_size, true);
      }
    }
  }
  if(is_font_legacy){
    cursor_x += 6 * font_size;
  }
  else{
    cursor_x += (glyph.xAdvance) * font_size;
  }
}

