void Adafruit_SSD1306::drawBitmap(int16_t x, int16_t y, const uint8_t bitmap[], int16_t w, int16_t h,
                                  uint16_t color, int strife, bool do_not_round)
{
  int i = 0;
  int16_t w_byte;
  if(do_not_round){
    w_byte = w;
  }
  else{
    w_byte = w & 7 ? (w + 8) & ~7 : w;
  }

  for(int y_i = 0; y_i < h; y_i += 1){
    for(int x_i = 0; x_i < w_byte; x_i += 1){
      if(bitmap[i>>3] & (0x80 >> (i & 7))){
        if(w == 8 && h == 5){
          drawPixel(x + y_i * strife, y + (w - x_i) * strife, color);
        }
        else{
          drawPixel(x + x_i * strife, y + y_i * strife, color);
        }
      }
      i += 1;
    }
  }
}
